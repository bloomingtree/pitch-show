/**
 * 分析 demo-song.mp3 并输出音符数据为 JSON
 * 使用 @spotify/basic-pitch + @tensorflow/tfjs
 */
import { BasicPitch, noteFramesToTime, outputToNotesPoly, addPitchBendsToNoteEvents } from '@spotify/basic-pitch'
import * as tf from '@tensorflow/tfjs'
import { readFileSync, writeFileSync } from 'fs'
import { resolve, dirname } from 'path'
import { fileURLToPath } from 'url'

const __dirname = dirname(fileURLToPath(import.meta.url))
const rootDir = resolve(__dirname, '..')

async function main() {
  const audioPath = resolve(rootDir, 'public/demo-song.mp3')
  const outputPath = resolve(rootDir, 'src/data/demoSongNotes.js')
  const modelPath = resolve(rootDir, 'public/model.json')

  console.log('Loading audio:', audioPath)
  const audioBuffer = readFileSync(audioPath)

  console.log('Loading TF model...')
  const model = await tf.loadGraphModel(`file://${modelPath}`)

  console.log('Creating BasicPitch instance...')
  const basicPitch = new BasicPitch(model)

  const frames = []
  const onsets = []
  const contours = []

  console.log('Running model evaluation...')
  await basicPitch.evaluateModel(
    audioBuffer.buffer.slice(audioBuffer.byteOffset, audioBuffer.byteOffset + audioBuffer.byteLength),
    (f, o, c) => {
      frames.push(...f)
      onsets.push(...o)
      contours.push(...c)
    },
    (p) => {
      process.stdout.write(`\rProgress: ${Math.round(p * 100)}%`)
    }
  )
  console.log('\nModel evaluation complete.')

  console.log('Processing notes...')
  const noteEvents = outputToNotesPoly(frames, onsets, 0.25, 0.25, 5)
  const notesWithBends = addPitchBendsToNoteEvents(contours, noteEvents)
  const notes = noteFramesToTime(notesWithBends)
  const sortedNotes = notes.sort((a, b) => {
    return (a.startTimeSeconds + a.durationSeconds) - (b.startTimeSeconds + b.durationSeconds)
  })

  console.log(`Total notes: ${sortedNotes.length}`)

  // Clean up notes for output - keep only essential fields
  const cleanNotes = sortedNotes.map(n => ({
    pitchMidi: n.pitchMidi,
    startTimeSeconds: Math.round(n.startTimeSeconds * 1000) / 1000,
    durationSeconds: Math.round(n.durationSeconds * 1000) / 1000,
    amplitude: Math.round((n.amplitude || 0.5) * 1000) / 1000,
    pitchBends: n.pitchBends ? n.pitchBends.map(b => Math.round((typeof b === 'object' ? b.pitchBend : b) * 1000) / 1000) : [],
    isDynamic: false
  }))

  // Calculate duration
  const maxEndTime = Math.max(...cleanNotes.map(n => n.startTimeSeconds + n.durationSeconds))
  const duration = Math.ceil(maxEndTime)

  // Generate JS file
  const output = `/**
 * Demo song note data - 永远的微笑 (Everlasting Smile) by 周璇
 * Generated by scripts/analyze-demo-song.mjs
 * Duration: ${duration}s
 */

export const DEMO_SONG_DURATION = ${duration}

export const DEMO_NOTES = ${JSON.stringify(cleanNotes, null, 2)}
`

  writeFileSync(outputPath, output, 'utf-8')
  console.log(`Notes written to: ${outputPath}`)
  console.log(`Duration: ${duration}s, Notes: ${cleanNotes.length}`)
}

main().catch(err => {
  console.error('Error:', err)
  process.exit(1)
})
